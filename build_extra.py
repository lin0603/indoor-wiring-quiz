# 更正原卷疑似答案錯誤（保守，僅限物理/法規明確者）
ANSWER_OVERRIDE = {
 "花蓮112-05": 1,  # 鉤式電流表一次側=被測導線=1匝；原卷標(2)疑為key誤植
}

# 少數答案與物理/法規判讀需留意者，加註於 exp 起頭
FLAG = {
 "花蓮112-05": "鉤式電流表一次側為被測導線穿越1匝；原卷答案疑誤，本專案更正為(1)。",
 "宜蘭113-01": "官方手寫答案判讀；地下電纜與管路交叉之埋設位置。",
 "宜蘭113-34": "3具10kVA變壓器總容量應為30kVA(選1)；原卷手寫較潦草，本題以物理正解為準。",
}

def build():
    recs = []
    # 花蓮 112/113/114（官方答案）
    for exam in ["花蓮112","花蓮113","花蓮114"]:
        for q in load_parsed(exam):
            if not (1 <= q["ans"] <= 4):
                continue
            recs.append(mk("花蓮", exam, q["num"], q["q"], q["opts"], q["ans"]))
    # 花蓮 115（推算答案，跳過第1題符號題）
    for q in load_parsed("花蓮115"):
        if q["num"] not in HL115_ANS:
            continue
        r = mk("花蓮", "花蓮115", q["num"], q["q"], q["opts"], HL115_ANS[q["num"]])
        r["guess"] = True
        recs.append(r)
    # 宜蘭 1-20（手寫官方答案）
    for q in load_parsed("宜蘭113"):
        if 1 <= q["ans"] <= 4:
            recs.append(mk("宜蘭", "宜蘭113", q["num"], q["q"], q["opts"], q["ans"]))
    # 宜蘭 21-40
    for num, qtext, opts, ans in ILAN_21_40:
        recs.append(mk("宜蘭", "宜蘭113", num, qtext, opts, ans))

    # 套用答案更正
    for r in recs:
        if r["id"] in ANSWER_OVERRIDE:
            r["ans"] = ANSWER_OVERRIDE[r["id"]]

    # 合併 exp / why
    why = {}
    for p in sorted(glob.glob(os.path.join(D, "extra_rationales_*.json"))):
        why.update(json.load(open(p, encoding="utf-8")))
    for r in recs:
        w = why.get(r["id"])
        base_exp = FLAG.get(r["id"], "")
        if w:
            r["exp"] = (base_exp + " " + w.get("exp", "")).strip()
            if isinstance(w.get("why"), list) and len(w["why"]) == 4:
                r["why"] = w["why"]
        else:
            r["exp"] = base_exp
        if r.pop("guess", False):
            r["exp"] = ("〔答案為本專案推算〕 " + r["exp"]).strip()

    # 一致性檢查：逐選項解析標「正確/正解」的位置需與 ans 相符
    issues = []
    for r in recs:
        w = r.get("why")
        if not w:
            continue
        marked = [i for i, t in enumerate(w) if re.match(r"^\s*(正確|正解)", t)]
        if marked and (r["ans"] - 1) not in marked:
            issues.append(f"{r['id']}: ans={r['ans']} 但解析標正確於選項{[m+1 for m in marked]}")
        elif len(marked) > 1:
            issues.append(f"{r['id']}: 多個選項被標為正確 {[m+1 for m in marked]}")

    json.dump(recs, open(os.path.join(D, "extra_questions.json"), "w", encoding="utf-8"),
              ensure_ascii=False, indent=1)
    from collections import Counter
    c = Counter(r["exam"] for r in recs)
    nwhy = sum(1 for r in recs if r.get("why"))
    print("extra_questions.json:", len(recs), "題", dict(c), "| 有逐選項解析", nwhy)
    if issues:
        print("⚠ 答案/解析不一致（需人工確認）:")
        for x in issues:
            print("   ", x)
    else:
        print("✓ 答案與逐選項解析一致")

def mk(site, exam, num, q, opts, ans):
    opts = [re.sub(r"\s+", "", o) for o in opts]
    return {"id": f"{exam}-{num:02d}", "site": site, "exam": exam, "num": num,
            "cat": categorize(q, opts), "q": re.sub(r"\s+", " ", q).strip(),
            "opts": opts, "ans": ans, "hot": False}

if __name__ == "__main__":
    build()
