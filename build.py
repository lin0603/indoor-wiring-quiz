def load_rationales():
    import glob
    why = {}
    for p in sorted(glob.glob(os.path.join(BASE, "data", "rationales_*.json"))):
        try:
            with open(p, encoding="utf-8") as f:
                data = json.load(f)
            for k, v in data.items():
                if isinstance(v, list) and len(v) == 4:
                    why[k] = v
        except Exception as e:
            print("!! 讀取", p, "失敗:", e)
    return why

def build_records():
    why_map = load_rationales()
    mem_map = load_mem()
    recs = []
    for exam, num, cat, q, opts, ans, exp in Q:
        assert len(opts) == 4, (exam, num)
        assert 1 <= ans <= 4, (exam, num)
        assert cat in CATS, (exam, num, cat)
        rid = f"{exam}-{num:02d}"
        rec = {
            "id": rid, "site": "五股",
            "exam": exam, "num": num, "cat": cat,
            "q": q, "opts": opts, "ans": ans, "exp": exp,
            "hot": rid in HOT_IDS,
        }
        if rid in why_map:
            rec["why"] = why_map[rid]
        if mem_map.get(rid):
            rec["mem"] = mem_map[rid]
        recs.append(rec)
    recs += load_extra()   # 花蓮 / 宜蘭 等其他場次（各自獨立、不併入五股統計）
    # 檢查五股每份考卷 40 題
    from collections import Counter
    five = [r for r in recs if r.get("site", "五股") == "五股"]
    c = Counter(r["exam"] for r in five)
    assert all(v == 40 for v in c.values()), c
    assert len(five) == 120, len(five)
    return recs

